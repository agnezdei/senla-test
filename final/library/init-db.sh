#!/bin/bash

set -e

echo "===== ИНИЦИАЛИЗАЦИЯ БАЗЫ ДАННЫХ С НУЛЯ ====="

# Останавливаем всё и удаляем ВСЕ volumes (включая данные PostgreSQL)
echo "Остановка и полное удаление контейнеров и volumes..."
docker-compose down -v

# Запускаем контейнеры заново (миграции применятся автоматически при старте приложения)
echo "Запуск контейнеров и ожидание применения миграций..."
docker-compose up -d

# Ждём, пока приложение поднимется и Flyway выполнит миграции
echo "Ожидание готовности приложения (20 секунд)..."
sleep 20

# Проверяем, что миграции применились (ищем в логах успешное сообщение)
if docker-compose logs app | grep -q "Schema \"public\" is up to date. No migration necessary\|Successfully applied"; then
    echo "Миграции успешно применены."
else
    echo "ВНИМАНИЕ: не удалось подтвердить применение миграций. Проверьте логи вручную."
fi

# Останавливаем контейнеры, но НЕ удаляем volume (чтобы данные сохранились)
echo "Остановка контейнеров (volume сохранён)..."
docker-compose down

echo "===== Инициализация завершена ====="
echo "Теперь вы можете запустить './run.sh' для обычного старта приложения с подготовленной базой."